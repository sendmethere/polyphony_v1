import { useState } from 'react'
import { useNavigate } from 'react-router-dom'

// 이미지 자리표시자. /public/help_image_N.png 를 넣으면 자동 표시되고,
// 없으면 캡션 박스만 보인다. (캡션 텍스트 = 어떤 장면을 넣을지 안내)
function Figure({ n, caption }) {
  const [err, setErr] = useState(false)
  return (
    <figure className="help-fig">
      {!err ? (
        <img src={`/help_image_${n}.png`} alt={caption} onError={() => setErr(true)} />
      ) : (
        <div className="help-fig-ph">이미지 {n} 자리</div>
      )}
      <figcaption>[이미지{n}: {caption}]</figcaption>
    </figure>
  )
}

